precedence = {"^": 3, "*": 2, "/": 2, "+": 1, "-": 1}

def is_operand(char):
    return char.isalpha() or char.isdigit()
def is_operator(char):
    return char in precedence
def has_higher_precedence(op1, op2):
    return precedence[op1] > precedence[op2]
def infix_to_postfix(infix):
    stack = []
    postfix = ""
    for char in infix:
        if is_operand(char):
            postfix += char
        elif is_operator(char):
            while stack and stack[-1] != "(" and not has_higher_precedence(char, stack[-1]):
                postfix += stack.pop()
            stack.append(char)
        elif char == "(":
            stack.append(char)
        elif char == ")":
            while stack and stack[-1] != "(":
                postfix += stack.pop()
            if stack:
                stack.pop()
    while stack:
        postfix += stack.pop()
    return postfix

